package viaggio.di.josh;

public class Mantello {
	
	private int intelligenza=0;
	private int difesa=0;
	private int difesaMagica=0;
	private int durabilita=0;
	
	public Mantello() {
		
	}

	public int getIntelligenza() {
		return intelligenza;
	}

	public void setIntelligenza(int intelligenza) {
		this.intelligenza = intelligenza;
	}

	public int getDifesa() {
		return difesa;
	}

	public void setDifesa(int difesa) {
		this.difesa = difesa;
	}

	public int getDifesaMagica() {
		return difesaMagica;
	}

	public void setDifesaMagica(int difesaMagica) {
		this.difesaMagica = difesaMagica;
	}

	public int getDurabilita() {
		return durabilita;
	}

	public void setDurabilita(int durabilita) {
		this.durabilita = durabilita;
	}
	
	

}
